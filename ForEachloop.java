class ForEachloop{
    public static void main(String [] args){
        //for interger type
        int arr[]={2,3,4,5};
        for(int i:arr){
            System.out.println(i);
        }
        //for string type 
        String [] name={"harshu","banty","bablu"};
        for(String j:name){
            System.out.println(j);
        }
    }
}